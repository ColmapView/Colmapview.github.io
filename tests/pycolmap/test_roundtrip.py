def _quat_xyzw_to_matrix(q: np.ndarray) -> np.ndarray:
    """Rotation matrix from a quaternion in (x, y, z, w) order."""
    x, y, z, w = q
    return np.array([
        [1 - 2 * (y * y + z * z), 2 * (x * y - z * w), 2 * (x * z + y * w)],
        [2 * (x * y + z * w), 1 - 2 * (x * x + z * z), 2 * (y * z - x * w)],
        [2 * (x * z - y * w), 2 * (y * z + x * w), 1 - 2 * (x * x + y * y)],
    ])


def _camera_world_position(img) -> np.ndarray:
    """Camera center in world space from a pycolmap Image."""
    # pycolmap 3.11: cam_from_world is a method returning a Rigid3d.
    cam_from_world = img.cam_from_world()
    q_xyzw = np.asarray(cam_from_world.rotation.quat)  # (x, y, z, w)
    t = np.asarray(cam_from_world.translation)
    R = _quat_xyzw_to_matrix(q_xyzw)
    # World position = -R^T @ t
    return -R.T @ t


def _apply_sim3d(sim3d: dict, p: np.ndarray) -> np.ndarray:
    """Apply a Sim3D transform P' = scale * R * P + t."""
    q = np.array(sim3d["rotation_quat_xyzw"])
    R = _quat_xyzw_to_matrix(q)
    t = np.array(sim3d["translation"])
    return sim3d["scale"] * (R @ p) + t


class TestCamerasMoveWithPoints:
    """After transform bake, cameras and 3D points are transformed by the same
    Sim3D. pycolmap should see each camera's world position at the expected
    location, and each 3D point at its expected location — so the relative
    geometry (distances, projection rays) is preserved. This guards against
    regressions where cameras and points drift apart after export."""

    @pytest.fixture(scope="class")
    def sim3d(self):
        sidecar = FIXTURES / "transform_baked" / "sim3d.json"
        return json.loads(sidecar.read_text())["sim3d"]

    @pytest.fixture(scope="class")
    def orig(self):
        return pycolmap.Reconstruction(str(_fixture_path("untransformed")))

    @pytest.fixture(scope="class")
    def baked(self):
        return pycolmap.Reconstruction(str(_fixture_path("transform_baked")))

    def test_camera_world_positions_match_expected(self, orig, baked, sim3d):
        # For every image, pycolmap's computed baked camera center should equal
        # Sim3D applied to the original camera center.
        assert set(orig.images) == set(baked.images)
        max_err = 0.0
        for image_id in orig.images:
            orig_C = _camera_world_position(orig.images[image_id])
            baked_C = _camera_world_position(baked.images[image_id])
            expected_C = _apply_sim3d(sim3d, orig_C)
            err = float(np.linalg.norm(baked_C - expected_C))
            max_err = max(max_err, err)
        assert max_err < 1e-6, f"camera positions drifted; max L2 err={max_err}"

    def test_point3d_positions_match_expected(self, orig, baked, sim3d):
        # Every surviving 3D point should be transformed by the same Sim3D.
        shared = set(orig.points3D) & set(baked.points3D)
        assert len(shared) > 1000, "too few shared point IDs — export reordered?"
        sampled = list(shared)[::100]  # every 100th point; cheap sanity
        max_err = 0.0
        for pid in sampled:
            orig_xyz = np.array(orig.points3D[pid].xyz)
            baked_xyz = np.array(baked.points3D[pid].xyz)
            expected_xyz = _apply_sim3d(sim3d, orig_xyz)
            err = float(np.linalg.norm(baked_xyz - expected_xyz))
            max_err = max(max_err, err)
        assert max_err < 1e-6, f"points3D drifted; max L2 err={max_err}"

    def test_camera_to_point_distances_scale_by_sim3d_scale(self, orig, baked, sim3d):
        # For any camera and any observed point, the ratio of baked distance to
        # original distance must equal sim3d.scale. This is the invariant a user
        # means when they say "cameras are aligned to the point cloud".
        scale = sim3d["scale"]
        sample_image_ids = list(orig.images)[:5]
        tolerances = []
        for image_id in sample_image_ids:
            orig_C = _camera_world_position(orig.images[image_id])
            baked_C = _camera_world_position(baked.images[image_id])
            orig_img_points = list(orig.images[image_id].points2D)
            if not orig_img_points:
                continue
            for p2d in orig_img_points[::200]:
                if p2d.point3D_id == 18446744073709551615 or p2d.point3D_id not in orig.points3D:
                    continue
                orig_P = np.array(orig.points3D[p2d.point3D_id].xyz)
                if p2d.point3D_id not in baked.points3D:
                    continue
                baked_P = np.array(baked.points3D[p2d.point3D_id].xyz)
                d_orig = float(np.linalg.norm(orig_P - orig_C))
                d_baked = float(np.linalg.norm(baked_P - baked_C))
                if d_orig < 1e-6:
                    continue
                ratio = d_baked / d_orig
                tolerances.append(abs(ratio - scale))
        assert tolerances, "no camera-point pairs found to check"
        assert max(tolerances) < 1e-5, (
            f"camera-to-point distances did not scale uniformly; max deviation "
            f"from sim3d.scale ({scale}) = {max(tolerances)}"
        )


class TestBugState:
    """Negative control: the pre-fix bug state must still be broken.

    The fixture reproduces the exact shape of the user's reported crash —
    images with empty points2D arrays plus points3D whose tracks reference
    those images. pycolmap refuses to construct a Reconstruction from it,
    throwing the exact `IndexError` the user saw ("invalid vector
    subscript" / `__n >= size() (0)`).

    If this test starts passing (i.e. no exception raised), one of the
    fixes regressed or the fixture generator stopped reproducing the bug.
    """

    def test_pycolmap_rejects_the_pre_fix_export(self):
        path = _fixture_path("bug_empty_points2D")
        with pytest.raises(IndexError):
            pycolmap.Reconstruction(str(path))
