def _count_dangling(rec: "pycolmap.Reconstruction") -> int:
    """Count track elements that reference (image_id, point2D_idx) out of range."""
    dangling = 0
    image_ids = set(rec.images)
    for pid, point in rec.points3D.items():
        for el in point.track.elements:
            if el.image_id not in image_ids:
                dangling += 1
                continue
            img = rec.images[el.image_id]
            if el.point2D_idx >= len(img.points2D):
                dangling += 1
    return dangling


